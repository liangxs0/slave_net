//CRC16检验
u16 crc16_check(u8 *buf, u8 len)//进行crc校验
{
	u16 uchCRC = 0xFFFF;
	u8 unindex,i,j;
	for(i=0;i<len;i++)
	{
		uchCRC = buf[i]^uchCRC;
		for(j=0;j<8;j++)
		{
			if(uchCRC & 0x01)
			{
				uchCRC >>= 1;
				uchCRC ^= 0xA001;
			}else{
				uchCRC >>= 1;
			}
		}
	}
	//高低位互换
	unindex = uchCRC>>8;
	uchCRC = unindex<<8|(uchCRC&0xFF);
	return uchCRC;
}



